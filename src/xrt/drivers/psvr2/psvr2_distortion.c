#include <stdint.h>
#include <stdbool.h>


struct LookupItem_t
{
	float r, g, b;
};
static const struct LookupItem_t lookup[1024];

void
psvr2_compute_distortion_asymmetric(float *calibration, struct xrt_uv_triplet *outCoords, int eEye, float fU, float fV)
{
	int lookup_index;
	float red_scale, green_scale, blue_scale;
	float local_x, local_y;
	float cx, cy = 0.5;
	float fx = 0.3585564;
	float fy = 0.3762281;

	float k1, k2, k3, k4;

	if (eEye == 0) {
		k1 = calibration[0];
		k2 = calibration[1];
		k3 = calibration[4];
		k4 = calibration[5];
		cx = 0.6603788;
	} else {
		k1 = calibration[2];
		k2 = calibration[3];
		k3 = calibration[6];
		k4 = calibration[7];
		cx = 0.3396213;
	}

	local_x = k1 + ((fU + fU) - 1.0) * 0.9803922 * 0.9394987;
	local_y = k2 + ((fV + fV) - 1.0) * 0.9394987;
	float Rscaled = (local_x * local_x + local_y * local_y) * 1023.0;
	lookup_index = (int)Rscaled;

	if (lookup_index > -1 && lookup_index + 1 < 0x400) {
		float frac = Rscaled - (float)lookup_index;

		const struct LookupItem_t t = lookup[lookup_index + 1];
		const struct LookupItem_t t_minus_one = lookup[lookup_index];

		red_scale = frac * t.r + (1. - frac) * t_minus_one.r;
		green_scale = frac * t.g + (1. - frac) * t_minus_one.g;
		blue_scale = frac * t.b + (1. - frac) * t_minus_one.b;
	} else {
		float v = Rscaled - 1023.0;

		red_scale = v * 0.003648639 + 1.592586;
		green_scale = v * 0.004862547 + 1.692979;
		blue_scale = v * 0.007425308 + 1.841771;
	}

	float vLUT[3] = {red_scale, green_scale, blue_scale};

	for (int c = 0; c < 3; c++) {
		float new_y = local_y;

		switch (c) {
		case 0: break;
		case 1: new_y -= 0.0002302693; break;
		case 2: break;
		}

		float a = local_x * vLUT[c];
		float b = new_y * vLUT[c];

		struct xrt_vec2 *coord = &((struct xrt_vec2 *)outCoords)[c];

		coord->x = (k3 * a - k4 * b) * fx + cx;
		coord->y = (k4 * a + k3 * b) * fy + cy;
	}
}

